import math

def area(r):
    '''Функция получает на вход число r (радиус круга), а на выходе возвращает площадь данного круга'''
    return math.pi * r * r


def perimeter(r):
    '''Функция получает на вход число r (радиус круга), а на выходе возвращает периметр данного круга'''
    return 2 * math.pi * r


